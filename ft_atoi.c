#include <libft.h>

int ft_atoi(char *str)
{
	int i;
	int sig;

	i = 0;
	sig = 1;
	while ((str[i] >= 9 && str[i] <= 13) || (str[i] == ' '))
		i++;
	if (str[i] == '-')
	{
		sig = -1;
	}
}